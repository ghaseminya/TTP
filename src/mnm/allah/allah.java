package mnm.allah;

public class allah {
	public String Name;
	public String E_Name;
	public String translat;
	public int soureh;
	public int ayeh;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getE_Name() {
		return E_Name;
	}
	public void setE_Name(String name) {
		E_Name = name;
	}
	public String getTranslat() {
		return translat;
	}
	public void setTranslat(String translat) {
		this.translat = translat;
	}
	public int getSoureh() {
		return soureh;
	}
	public void setSoureh(int soureh) {
		this.soureh = soureh;
	}
	public int getAyeh() {
		return ayeh;
	}
	public void setAyeh(int ayeh) {
		this.ayeh = ayeh;
	}
	
	
}
